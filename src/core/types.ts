/**
 * Direction from which the drawer emerges and the direction to swipe to dismiss it.
 * - 'down': Drawer emerges from bottom, swipe down to dismiss
 * - 'up': Drawer emerges from top, swipe up to dismiss
 * - 'left': Drawer emerges from left, swipe left to dismiss
 * - 'right': Drawer emerges from right, swipe right to dismiss
 */
export type DismissalDirection = 'up' | 'down' | 'left' | 'right'
